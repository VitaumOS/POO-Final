    /**
     * Cria uma nova janela de resumo do dia.
     * Inicializa os componentes da interface e preenche a tabela com 
     * os pedidos diários obtidos do gerenciador de diário da pizzaria.
     *
     * @param pizzaria Instância da pizzaria que contém os pedidos do dia.
     */
    public ResumoPedidosUI(Pizzaria pizzaria) {
        initComponents();
        
        // Percorre todos os pedidos do dia e os adiciona na tabela
        for (int i = 0; i < pizzaria.getGerenciadorDiario().getPedidosDiarios().size(); i++) {
            Pedido pedido = pizzaria.getGerenciadorDiario().getPedidosDiarios().get(i);
            String[] aux = new String[3];
            // Processa os acompanhamentos, ignorando entradas "Nenhum"
            for(int j=0; j<3; j++){
                if(!pedido.getAcompanhamentos(j).equals("Nenhum"))
                    aux[j] = pedido.getAcompanhamentos(j);
                else
                    aux[j]="";
            }
            // Concatena os acompanhamentos em uma única string
            String acompanhamento = aux[0]+" "+aux[1]+" " + aux[2];
            // Preenche a linha da tabela com os dados do pedido
            tabela_resumodia.setValueAt(pedido.getNomeCliente(), i, 0);
            tabela_resumodia.setValueAt(pedido.getPizza(), i, 1);
            tabela_resumodia.setValueAt(pedido.getBorda(), i, 2);
            tabela_resumodia.setValueAt(acompanhamento, i, 3);
            tabela_resumodia.setValueAt("R$"+pedido.getValorTotal()+"0", i, 4);    
        }
    }

